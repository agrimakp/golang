package main

// brute force results ( algorithm has O(n^2) time complexity
// Runtime: 30 ms, Memory Usage: 3.5 MB
func twoSum(nums []int, target int) []int {
	for i := 0; i < len(nums); i++ {
		for j := i + 1; j < len(nums); j++ {
			if nums[i]+nums[j] == target {
				return []int{i, j}
			}
		}
	}
	return []int{}
}
